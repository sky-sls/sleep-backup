def entry_func(args=None):
    parser = get_argparser()
    args = parser.parse_args(args)

    if args.cv:
        from ustaging.hyperparameters import YAMLHParams
        project_dir = os.path.abspath("./")
        hparams = YAMLHParams(ustaging.Defaults.get_hparams_path(project_dir))

        for fold in range(10):
            print(f"Starting fold {fold}")

            update_hparams_with_fold(hparams, fold)

            out_dir = f"data/fold_{fold}"
            os.makedirs(out_dir, exist_ok=True)

            import subprocess
            preprocess_cmd = [
                "python", "../../ustaging/bin/preprocess.py",
                "--out_path", f"{out_dir}/processed_data.h5",
                "--dataset_splits", "train_data", "val_data",
                "--overwrite"
            ]
            print(f"Running preprocess for fold {fold}: {' '.join(preprocess_cmd)}")
            result = subprocess.run(preprocess_cmd, cwd=project_dir)
            if result.returncode != 0:
                print(f"Preprocess failed for fold {fold}")
                continue

            args_fold = copy.deepcopy(args)
            args_fold.fold = fold
            args_fold.continue_training = False
