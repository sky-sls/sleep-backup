def get_argparser():
    """
    Returns an argument parser for this script
    """
    parser = ArgumentParser(description='Fit a U-Time model defined in'
                                        ' a project folder. Invoke '
                                        '"ut init" to start a new project.')
    parser.add_argument("--num_GPUs", type=int, default=1,
                        help="Number of GPUs to use for this job (default=1)")
    parser.add_argument("--force_GPU", type=str, default="",
                        help="GPU ids to force, e.g. '0' or '0,1'")
    parser.add_argument("--continue_training", action="store_true",
                        help="Continue the last training session")
    parser.add_argument("--initialize_from", type=str, default=None,
                        help="Path to a model weights file to initialize from.")
    parser.add_argument("--log_file_prefix", type=str,
                        help="Optional prefix for logfiles.", default="")
    parser.add_argument("--overwrite", action='store_true',
                        help='overwrite previous training session in the '
                             'project path')
    parser.add_argument("--just", type=int, default=None,
                        help="For testing purposes, run only on the first "
                             "[just] training and validation samples.")
    parser.add_argument("--no_val", action="store_true",
                        help="For testing purposes, do not perform validation.")
    parser.add_argument("--max_train_samples_per_epoch", type=int,
                        default=int(5e5),
                        help="Maximum number of sleep stages to sample in each "
                             "epoch. (defaults to 5e5)")
    parser.add_argument("--n_epochs", type=int, default=None,
                        help="Overwrite the number of epochs specified in the "
                             "hyperparameter file with this number (int).")
    parser.add_argument("--channels", nargs='*', type=str, default=None,
                        help="A list of channels to use instead of those "
                             "specified in the parameter file.")
    parser.add_argument("--train_queue_type", type=str, default='eager',
                        help="Data queueing type for training data. One of: "
                             "'eager', 'lazy', 'limitation'. The 'eager' "
                             "queue loads all data into memory up front, the "
                             "'lazy' queue loads data only when needed and the "
                             "'limitation' queue maintains a buffer of loaded "
                             "data limited according to "
                             "--max_loaded_per_dataset and "
                             "--num_access_before_reload which is recycled "
                             "continuously. Note: with --preprocessed, the "
                             "'eager' queue is always used, as data is not "
                             "loaded from the HDF5 archive even with calls to "
                             ".load() methods.")
    parser.add_argument("--val_queue_type", type=str, default='lazy',
                        help="Queue type for validation data. "
                             "See --train_queue_type.")
    parser.add_argument("--max_loaded_per_dataset", type=int, default=40,
                        help="Set a number of maximum SleepStudy objects to "
                             "be kept loaded in memory at any given time per "
                             "dataset. OBS: If training on multiple datasets, "
                             "this means that 'max_loaded_per_dataset' times "
                             "the number of datasets will be the number of "
                             "studies loaded at a given time.")
    parser.add_argument("--num_access_before_reload", type=int, default=32,
                        help="If --max_loaded_per_dataset is set, this value "
                             "determines how many times a SleepStudy object "
                             "is accessed (normally for extracting data) "
                             "before the study is unloaded and replaced by "
                             "another random study from the same dataset.")
    parser.add_argument("--preprocessed", action='store_true',
                        help="Run on a pre-processed dataset as output by the "
                             "'ut preprocess' script. Streams data from disk "
                             "which significantly reduces memory load. "
                             "However, running with --preprocessed ignores "
                             "settings such as 'strip_func', "
                             "'quality_control_function', 'scaler' etc. "
                             "(those were applied in the preprocess step). "
                             "Changes to any of those parameters requires a "
                             "rerun of 'ut preprocess' to be effective with "
                             "the 'ut train' --preprocessed flag.")
    parser.add_argument("--fold", type=int, default=None,
                        help="Fold number for cross-validation (0-9). "
                             "If None, use single fold.")
    parser.add_argument("--cv", action="store_true",
                        help="Run 10-fold cross-validation.")
    parser.add_argument("--train_on_val", action="store_true",
                        help="Include the validation set in the training set. "
                             "Will force --no_val to be active.")
    return parser


def assert_args(args):
    """
    Validate command-line arguments for training / CV.
    Raise ValueError with clear message if any conflict is found.
    """
    if args.num_GPUs < 1:
        raise ValueError("--num_GPUs must be >= 1")

    if args.n_epochs is not None and args.n_epochs < 1:
        raise ValueError("--n_epochs must be >= 1")

    if args.just is not None and args.just < 1:
        raise ValueError("--just must be >= 1")

    if args.max_train_samples_per_epoch is not None and args.max_train_samples_per_epoch < 1:
        raise ValueError("--max_train_samples_per_epoch must be >= 1")

    if args.fold is not None and not (0 <= args.fold <= 9):
        raise ValueError("--fold must be in [0, 9]")

    if args.cv and args.fold is not None:
        raise ValueError("Do not set both --cv and --fold at the same time.")

    if args.train_on_val:
        args.no_val = True

    valid_queue_types = {"eager", "lazy", "limitation"}
    if args.train_queue_type not in valid_queue_types:
        raise ValueError(
            f"--train_queue_type must be one of {valid_queue_types}, "
            f"got '{args.train_queue_type}'"
        )
    if args.val_queue_type not in valid_queue_types:
        raise ValueError(
            f"--val_queue_type must be one of {valid_queue_types}, "
            f"got '{args.val_queue_type}'"
        )

    if args.force_GPU:
        try:
            _ = [int(x.strip()) for x in args.force_GPU.split(",") if x.strip() != ""]
        except Exception as e:
            raise ValueError(
                "--force_GPU must be a comma-separated list of integers, "
                "e.g. '0' or '0,1'"
            ) from e

    project_dir = os.path.abspath("./")

    if args.preprocessed:
        if args.fold is not None:
            fold_h5 = os.path.join(project_dir, f"data/fold_{args.fold}/processed_data.h5")
            if not os.path.exists(fold_h5):
                raise ValueError(
                    f"--preprocessed with --fold {args.fold} requires file:\n"
                    f"{fold_h5}\n"
                    f"but it does not exist."
                )
        elif not args.cv:
            default_h5 = os.path.join(project_dir, "data/processed_data.h5")
            if not os.path.exists(default_h5):
                raise ValueError(
                    f"--preprocessed requires preprocessed H5 file:\n"
                    f"{default_h5}\n"
                    f"but it does not exist."
                )

    if args.cv and args.no_val:
        print("[WARNING] --no_val is ignored in --cv mode if fold training uses val_data.")


def update_preprocessed_hparams_with_fold(hparams, fold, project_dir, logger=None):
    """
    For --preprocessed + --fold:
    1) rewrite dataset sub-yaml(s) so train/val/test point to data/fold_{fold}/processed_data.h5
    2) rewrite output paths in main pre_proc_hparams.yaml so different folds do not collide
    """
    from ustaging.utils.scriptutils import get_all_dataset_hparams

    fold_h5 = os.path.abspath(os.path.join(project_dir, f"data/fold_{fold}/processed_data.h5"))
    if not os.path.exists(fold_h5):
        raise FileNotFoundError(f"Fold H5 not found: {fold_h5}")

    for dataset_id, dataset_hparams in get_all_dataset_hparams(hparams).items():
        if "train_data" in dataset_hparams:
            dataset_hparams["train_data"]["data_dir"] = fold_h5
        if "val_data" in dataset_hparams:
            dataset_hparams["val_data"]["data_dir"] = fold_h5
        if "test_data" in dataset_hparams:
            dataset_hparams["test_data"]["data_dir"] = fold_h5

        dataset_hparams.save_current()

        msg = (
            f"[INFO] Updated dataset yaml for {dataset_id}: "
            f"train={dataset_hparams.get('train_data', {}).get('data_dir', None)}, "
            f"val={dataset_hparams.get('val_data', {}).get('data_dir', None)}, "
            f"test={dataset_hparams.get('test_data', {}).get('data_dir', None)}"
        )
        if logger:
            logger(msg)
        else:
            print(msg)

    if "__CB_tb" in hparams:
        hparams["__CB_tb"]["kwargs"]["log_dir"] = f"./tensorboard/fold_{fold}"

    if "__CB_mcp_clean" in hparams:
        hparams["__CB_mcp_clean"]["kwargs"]["filepath"] = \
            f"./model/fold_{fold}_@epoch_{{epoch:02d}}_val_dice_{{val_dice:.5f}}.h5"

    if "__CB_csv" in hparams:
        hparams["__CB_csv"]["kwargs"]["filename"] = f"logs/fold_{fold}_training.csv"

    hparams.save_current()

    if logger:
        logger(f"[INFO] Updated main preprocessed hparams outputs for fold {fold}")
    else:
        print(f"[INFO] Updated main preprocessed hparams outputs for fold {fold}")


def update_hparams_with_fold(hparams, fold):
    """
    Update hparams data directories for the given fold in cross-validation.
    """
    base_path = "/export/SHELAB/sikaiyue/sleep/TinyUStaging_sky/demo/datasets/processed_SKY/dcsm/views/10_CV/split_{}".format(fold)
    from ustaging.utils.scriptutils import get_all_dataset_hparams

    for _, dataset_hparams in get_all_dataset_hparams(hparams).items():
        if 'train_data' in dataset_hparams:
            dataset_hparams['train_data']['data_dir'] = "{}/train".format(base_path)
        if 'val_data' in dataset_hparams:
            dataset_hparams['val_data']['data_dir'] = "{}/val".format(base_path)
        if 'test_data' in dataset_hparams:
            dataset_hparams['test_data']['data_dir'] = "{}/test".format(base_path)
        dataset_hparams.save_current()

    if '__CB_tb' in hparams:
        hparams['__CB_tb']['kwargs']['log_dir'] = f"./tensorboard/fold_{fold}"

    if '__CB_mcp_clean' in hparams:
        hparams['__CB_mcp_clean']['kwargs']['filepath'] = \
            f"./model/fold_{fold}_@epoch_{{epoch:02d}}_val_dice_{{val_dice:.5f}}.h5"

    if '__CB_csv' in hparams:
        hparams['__CB_csv']['kwargs']['filename'] = f"logs/fold_{fold}_training.csv"

    hparams.save_current()


def update_hparams_with_command_line_arguments(hparams, args):
    """
    Overwrite hyperparameters stored in YAMLHparams object 'hparams'
    according to passed args.
    """
    if isinstance(args.n_epochs, int) and args.n_epochs > 0:
        hparams.set_value(subdir="fit",
                          name="n_epochs",
                          value=args.n_epochs,
                          overwrite=True)
        hparams["fit"]["n_epochs"] = args.n_epochs

    if args.channels is not None and args.channels:
        from ustaging.utils.scriptutils import get_all_dataset_hparams
        for _, dataset_hparams in get_all_dataset_hparams(hparams).items():
            dataset_hparams.set_value(subdir=None,
                                      name="select_channels",
                                      value=args.channels,
                                      overwrite=True)
            if dataset_hparams.get("load_time_channel_sampling_groups"):
                dataset_hparams.delete_group('load_time_channel_sampling_groups')
            if dataset_hparams.get("access_time_channel_sampling_groups"):
                dataset_hparams.delete_group('access_time_channel_sampling_groups')
            dataset_hparams.save_current()

    hparams.save_current()


def keep_n_random(*datasets, keep, logger):
    """
    Keep only N random studies from each dataset
    """
    logger("Keeping only {} random study in each dataset (--just)...".format(keep))
    for dataset in datasets:
        dataset._pairs = np.random.choice(dataset.pairs, keep, replace=False)
        dataset.update_id_to_study_dict()


